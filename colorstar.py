import turtle
import time
screen=turtle.Screen()
screen.setup(450,350)
screen.bgcolor('black')

ym=turtle.Turtle()
color_set=['red','green','yellow','blue','purple']
ym.pensize(5)
ym.penup()
ym.setpos(-90,30)
ym.pendown()
for i in range(5):
    ym.pencolor(color_set[i])
    ym.fd(100)
    ym.lt(144)
ym.penup()
ym.setpos(80,150)
ym.pendown
ym.ht()
    




